#!/usr/bin/env node
import { writeFileSync } from 'fs';
import { resolve, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const DIR = resolve(__dirname, '..', 'client', 'src', 'data', 'translations', 'trivia');

function write(locale, exportName, data) {
  const lines = [`export const ${exportName}: Record<string, string> = {`];
  for (const [slug, text] of Object.entries(data)) {
    const escaped = text.replace(/\\/g, '\\\\').replace(/'/g, "\\'");
    lines.push(`  '${slug}': '${escaped}',`);
  }
  lines.push('};\n');
  writeFileSync(resolve(DIR, `${locale}.ts`), lines.join('\n'), 'utf-8');
  console.log(`✅ trivia/${locale}.ts — ${Object.keys(data).length} entries`);
}

